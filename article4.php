					<h2>BSSN Luncurkan Platform "CekFakta AI" untuk Tangkal Penipuan Deepfake</h2>
					<p id="isi">
                       Menanggapi maraknya kasus penipuan online yang memanfaatkan teknologi kecerdasan buatan (AI), Badan Siber dan Sandi Negara (BSSN) hari ini secara resmi meluncurkan platform digital bernama "CekFakta AI". Platform ini dirancang khusus untuk membantu masyarakat mengidentifikasi konten video dan audio yang telah dimanipulasi menggunakan teknologi deepfake.
Kepala BSSN, Hinsa Siburian, dalam acara peluncuran di Jakarta, menyatakan bahwa CekFakta AI merupakan langkah proaktif pemerintah untuk melindungi ruang digital Indonesia dari ancaman disinformasi dan kejahatan siber. "Teknologi AI berkembang sangat pesat. Di satu sisi membawa manfaat, di sisi lain menciptakan celah baru bagi pelaku kejahatan. Platform ini adalah alat bagi publik untuk memverifikasi keaslian konten yang mereka terima," jelasnya.
Cara kerja CekFakta AI cukup sederhana. Pengguna dapat mengunggah file video, audio, atau menempelkan tautan dari media sosial ke dalam platform. Sistem kemudian akan menganalisis konten tersebut menggunakan algoritma machine learning untuk mendeteksi anomali dan tanda-tanda manipulasi digital. Hasil analisis akan ditampilkan dalam persentase tingkat kemungkinan rekayasa. BSSN juga menggandeng beberapa universitas dan komunitas fact-checker untuk terus memperbarui database dan meningkatkan akurasi sistem.
